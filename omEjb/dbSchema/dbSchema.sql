            SET status = i_new_state, status_comment = i_comment
          WHERE ID = i_tx_id;
      END IF;
   END change_tx_state;

   /**
   Checks if new entities may be inserted in the given transaction_id and locks it
   in case of success to prevent status changes to the same transaction of a concurrent session.
   */
   FUNCTION check_tx_insertable (i_tx_id     IN NUMBER,
                                 i_tx_type   IN VARCHAR2,
                                 i_client    IN VARCHAR2)
      RETURN BOOLEAN
   AS
      l_valid   NUMBER := 0;
   BEGIN
      BEGIN
             SELECT 1
               INTO l_valid
               FROM TRANSACTION T
              WHERE     T.ID = i_tx_id
                    AND T.ENTITYTYPE = i_tx_type
                    AND T.CLIENT_ID = i_client
                    AND T.STATUS IN ('NEW', 'EDITING')
         FOR UPDATE OF T.STATUS;
      EXCEPTION
         WHEN NO_DATA_FOUND
         THEN
            l_valid := 0;
      END;

      IF l_valid = 1
      THEN
         RETURN TRUE;
      ELSE
         RETURN FALSE;
      END IF;
   END check_tx_insertable;

   /**
   Checks if this transactionid may be replaced by another one.
   */
   FUNCTION check_tx_replaceable (i_tx_id IN NUMBER)
      RETURN BOOLEAN
   AS
      l_replaceable   NUMBER := 0;
   BEGIN
      BEGIN
         SELECT 1
           INTO l_replaceable
           FROM    TRANSACTION T
                LEFT OUTER JOIN
                   transactionstatus_change tc
                ON T.status = tc.old_state
          WHERE ID = i_tx_id
                AND (tc.old_state IS NULL
                     OR tc.old_state IN ('NEW', 'EDITING'));
      EXCEPTION
         WHEN NO_DATA_FOUND
         THEN
            l_replaceable := 0;
      END;

      IF l_replaceable = 1
      THEN
         RETURN TRUE;
      ELSE
         RETURN FALSE;
      END IF;
   END check_tx_replaceable;

   FUNCTION check_state_change (i_tx_id IN NUMBER, i_new_state IN VARCHAR2)
      RETURN NUMBER
   AS
      l_result   NUMBER;
   BEGIN
      SELECT COUNT (*)
        INTO l_result
        FROM    TRANSACTION T
             INNER JOIN
                transactionstatus_change tc
             ON T.STATUS = TC.OLD_STATE
       WHERE TC.NEW_STATE = i_new_state AND T.ID = i_tx_id;

      IF l_result > 0
      THEN
         RETURN 1;
      ELSE
         RETURN 0;
      END IF;
   END check_state_change;

   FUNCTION check_state_change (i_old_state   IN VARCHAR2,
                                i_new_state   IN VARCHAR2)
      RETURN NUMBER
   AS
      l_result   NUMBER := 0;
   BEGIN
      IF i_old_state = i_new_state
      THEN
         RETURN 1;
      END IF;

      BEGIN
         SELECT 1
           INTO l_result
           FROM transactionstatus_change tc
          WHERE TC.OLD_STATE = i_old_state AND TC.NEW_STATE = i_new_state;

         RETURN 1;
      EXCEPTION
         WHEN NO_DATA_FOUND
         THEN
            RETURN 0;
      END;
   END check_state_change;

   PROCEDURE entity_tx_trigger_template (i_inserting          IN BOOLEAN,
                                         i_updating           IN BOOLEAN,
                                         i_deleting           IN BOOLEAN,
                                         i_old_tx_id          IN NUMBER,
                                         i_new_tx_id          IN NUMBER,
                                         i_tx_type            IN VARCHAR2,
                                         i_client             IN VARCHAR2,
                                         i_old_entity_state   IN VARCHAR2,
                                         i_new_entity_state   IN VARCHAR2)
   AS
   BEGIN
      DBMS_SESSION.SET_IDENTIFIER (USER);

      IF get_is_reverting_tx OR get_is_reverting_entity
      THEN
         RETURN;
      END IF;

      IF i_inserting
      THEN
         IF NOT check_tx_insertable (i_tx_id     => i_new_tx_id,
                                     i_tx_type   => i_tx_type,
                                     i_client    => i_client)
         THEN
            RAISE_APPLICATION_ERROR (
               -20300,
               'Transaction IS NOT IN an insertable state!');
         ELSIF i_new_entity_state != 'NEW'
         THEN
            RAISE_APPLICATION_ERROR (-20100, 'NEW Entity state IS NOT NEW!');
         END IF;
      ELSIF i_updating
      THEN
         DECLARE
            l_count_tx   NUMBER;
         BEGIN
            SELECT COUNT (*)
              INTO l_count_tx
              FROM transaction
             WHERE status IN ('APPROVED', 'ALLOCATED') AND ID = i_new_tx_id;

            IF i_new_entity_state IN ('ALLOCATED', 'DEACTIVATED')
               AND l_count_tx > 0
            THEN
               RETURN;
            END IF;
         EXCEPTION
            WHEN NO_DATA_FOUND
            THEN
               RAISE_APPLICATION_ERROR (-20300,
                                        'Transaction is not allocated!');
         END;

         IF NOT CHECK_TX_INSERTABLE (i_tx_id     => i_new_tx_id,
                                     i_tx_type   => i_tx_type,
                                     i_client    => i_client)
         THEN
            RAISE_APPLICATION_ERROR (
               -20300,
               'Transaction IS NOT IN an insertable state!');
         ELSIF i_old_tx_id != i_new_tx_id
               AND NOT check_tx_replaceable (i_tx_id => i_old_tx_id)
         THEN
            RAISE_APPLICATION_ERROR (
               -20200,
               'OLD Transaction could NOT be replaced because it IS NOT yet finalized!');
         ELSIF check_tx_insertable (i_tx_id     => i_new_tx_id,
                                    i_tx_type   => i_tx_type,
                                    i_client    => i_client)
               AND i_new_entity_state NOT IN
                      ('NEW', 'EDITING', 'MARKED_FOR_DEACTIVATION')
         THEN
            RAISE_APPLICATION_ERROR (-20100, 'Entity state IS NOT valid!');
         ELSIF i_new_tx_id IS NULL
               AND NOT check_tx_replaceable (i_tx_id => i_old_tx_id)
         THEN
            RAISE_APPLICATION_ERROR (
               -20300,
               'Transaction IS NOT AT THE END OF its lifecycle AND cannot be deleted.');
         ELSE
            NULL;                                         -- everything is ok.
         END IF;
      ELSIF i_deleting AND i_old_entity_state != 'DELETED'
      THEN
         RAISE_APPLICATION_ERROR (
            -20200,
            'Entity IS NOT AT THE END OF its lifecycle AND cannot be deleted.');
      END IF;
   END entity_tx_trigger_template;

   PROCEDURE discard_single_record (i_table        IN VARCHAR2,
                                    i_id           IN NUMBER,
                                    i_target_scn   IN NUMBER)
   AS
      l_sqlStatement   VARCHAR2 (4000 CHAR);
      l_count          NUMBER;
      l_columns        VARCHAR2 (4000 CHAR) := '';
      l_sep            VARCHAR2 (2 CHAR) := '';
   BEGIN
      l_sqlStatement :=
            'SELECT COUNT(*) FROM '
         || DBMS_ASSERT.SQL_OBJECT_NAME (i_table)
         || ' AS OF SCN '
         || i_target_scn
         || ' WHERE ID=:ID ';
      DBMS_OUTPUT.put_line (l_sqlStatement || ' id: ' || i_id);

      EXECUTE IMMEDIATE l_sqlStatement INTO l_count USING i_id;

      IF l_count = 0
      THEN
         -- record was inserted.
         l_sqlStatement :=
               'DELETE FROM '
            || DBMS_ASSERT.SQL_OBJECT_NAME (i_table)
            || ' WHERE ID=:ID';
         DBMS_OUTPUT.put_line (l_sqlStatement || ' id: ' || i_id);

         EXECUTE IMMEDIATE l_sqlStatement USING i_id;

         RETURN;
      END IF;

      l_sqlStatement :=
            'SELECT COUNT(*) FROM '
         || DBMS_ASSERT.SQL_OBJECT_NAME (i_table)
         || ' E '
         || 'WHERE E.ID=:ID ';
      DBMS_OUTPUT.put_line (l_sqlStatement || ' id: ' || i_id);

      EXECUTE IMMEDIATE l_sqlStatement INTO l_count USING i_id;

      IF l_count = 0
      THEN
         -- entity was deleted
         l_sqlStatement :=
               'INSERT INTO '
            || DBMS_ASSERT.SQL_OBJECT_NAME (i_table)
            || ' SELECT * FROM '
            || DBMS_ASSERT.SQL_OBJECT_NAME (i_table)
            || ' AS OF SCN '
            || i_target_scn
            || ' WHERE ID = :ID';
         DBMS_OUTPUT.put_line (l_sqlStatement || ' id: ' || i_id);

         EXECUTE IMMEDIATE l_sqlStatement USING i_id;

         RETURN;
      END IF;

      -- entity was updated.
      FOR l_row
         IN (SELECT *
               FROM user_tab_columns
              WHERE table_name = DBMS_ASSERT.SQL_OBJECT_NAME (i_table))
      LOOP
         l_columns := l_columns || l_sep || l_row.column_name;
         l_sep := ', ';
      END LOOP;

      l_sqlStatement :=
            'UPDATE '
         || DBMS_ASSERT.SQL_OBJECT_NAME (i_table)
         || ' SET ('
         || l_columns
         || ') = (SELECT '
         || l_columns
         || ' FROM '
         || DBMS_ASSERT.SQL_OBJECT_NAME (i_table)
         || ' AS OF SCN '
         || i_target_scn
         || ' WHERE ID=:i_id) WHERE ID=:i_id';
      DBMS_OUTPUT.put_line (l_sqlStatement || ' id: ' || i_id);

      EXECUTE IMMEDIATE l_sqlStatement USING i_id, i_id;
   END discard_single_record;

   PROCEDURE discard_sub_records (i_type                IN VARCHAR2,
                                  i_table               IN VARCHAR2,
                                  i_constraint_column   IN VARCHAR2,
                                  i_parent_id           IN NUMBER,
                                  i_target_scn          IN NUMBER)
   AS
      l_sqlStatement   VARCHAR2 (4000 CHAR)
         :=    'SELECT DISTINCT ID FROM '
            || i_table
            || ' VERSIONS BETWEEN SCN MINVALUE AND MAXVALUE WHERE '
            || i_constraint_column
            || ' = '
            || i_parent_id;
      l_dyn_cursor     SYS_REFCURSOR;
      l_current_id     NUMBER;
   BEGIN
      OPEN l_dyn_cursor FOR (l_sqlStatement);

      FETCH l_dyn_cursor INTO l_current_id;

      WHILE l_dyn_cursor%FOUND
      LOOP
         discard_single_record (i_table        => i_table,
                                i_id           => l_current_id,
                                i_target_scn   => i_target_scn);

         FOR l_row
            IN (    SELECT fk.table_name fk_table,
                           pt.table_name p_table,
                           FKC.COLUMN_NAME fk_column,
                           PtC.COLUMN_NAME p_column
                      FROM constraints_hlp fk
                           INNER JOIN CONS_COLUMNS_hlp fkc
                              ON FKC.CONSTRAINT_NAME = FK.CONSTRAINT_NAME
                           INNER JOIN constraints_hlp pk
                              ON     fk.table_name = pk.table_name
                                 AND fk.CONSTRAINT_TYPE = 'R'
                                 AND pk.CONSTRAINT_TYPE = 'P'
                           INNER JOIN constraints_hlp pt
                              ON FK.R_CONSTRAINT_NAME = PT.CONSTRAINT_NAME
                           INNER JOIN CONS_COLUMNS_hlp ptc
                              ON Ptc.CONSTRAINT_NAME = Pt.CONSTRAINT_NAME
                                 AND FKC.POSITION = PtC.POSITION
                           INNER JOIN ADD_DISCARD_TABLES c
                              ON fk.table_name = c.table_name
                                 AND C.ENTITY_TYPE = i_type
                     WHERE LEVEL = 1
                CONNECT BY fk.R_CONSTRAINT_NAME = PRIOR pk.CONSTRAINT_NAME
                START WITH pt.TABLE_NAME = i_table)
         LOOP
            discard_sub_records (i_type                => i_type,
                                 i_table               => l_row.fk_table,
                                 i_constraint_column   => l_row.fk_column,
                                 i_parent_id           => l_current_id,
                                 i_target_scn          => i_target_scn);
         END LOOP;

         FETCH l_dyn_cursor INTO l_current_id;
      END LOOP;

      CLOSE l_dyn_cursor;
   END discard_sub_records;

   PROCEDURE discard_entity (i_type IN VARCHAR2, i_id IN NUMBER)
   AS
      l_sqlStatement   VARCHAR2 (4000 CHAR);
      l_scn            NUMBER;
      l_endscn         NUMBER;
   BEGIN
      IF i_type IS NULL OR i_id IS NULL
      THEN
         RAISE_APPLICATION_ERROR (-20000, 'Invalid argument');
      END IF;

      set_is_reverting_entity (TRUE);

      BEGIN
         IF i_type IN ('TSP', 'SHIPPER', 'FMPARTNER')
         THEN
            l_sqlStatement :=
               'SELECT SCN, END_SCN FROM (SELECT VERSIONS_STARTSCN SCN, VERSIONS_ENDSCN END_SCN FROM '
               || 'PARTNER VERSIONS BETWEEN SCN MINVALUE AND MAXVALUE E '
               || 'WHERE E.ID=:ID AND (STATUS IS NULL OR STATUS=''ALLOCATED'')'
               || 'ORDER BY VERSIONS_STARTSCN DESC NULLS LAST) '
               || 'WHERE ROWNUM = 1';
         ELSE
            l_sqlStatement :=
               'SELECT SCN, END_SCN FROM (SELECT VERSIONS_STARTSCN SCN, VERSIONS_ENDSCN END_SCN FROM '
               || DBMS_ASSERT.SQL_OBJECT_NAME (i_type)
               || ' VERSIONS BETWEEN SCN MINVALUE AND MAXVALUE E '
               || 'WHERE E.ID=:ID AND (STATUS IS NULL OR STATUS=''ALLOCATED'') '
               || 'ORDER BY VERSIONS_STARTSCN DESC NULLS LAST) '
               || 'WHERE ROWNUM = 1';
         END IF;

         EXECUTE IMMEDIATE l_sqlStatement INTO l_scn, l_endscn USING i_id;

         IF l_scn IS NULL
         THEN
            l_scn := l_endscn - 1;
         END IF;

         DBMS_OUTPUT.put_line ('Target-SCN: ' || l_scn);
      EXCEPTION
         WHEN NO_DATA_FOUND
         THEN
            -- Entity was not yet allocated => simply delete it from the database.
            -- Children get cleaned up by the referential constraints.
            IF i_type IN ('TSP', 'SHIPPER', 'FMPARTNER')
            THEN
               l_sqlStatement := 'DELETE FROM PARTNER WHERE ID=:ID';
            END IF;

            l_sqlStatement :=
                  'DELETE FROM '
               || DBMS_ASSERT.SQL_OBJECT_NAME (i_type)
               || ' WHERE ID=:ID';

            EXECUTE IMMEDIATE l_sqlStatement USING i_id;

            DBMS_OUTPUT.put_line (i_type || ': ID: ' || i_id || ' deleted!');
            set_is_reverting_entity (FALSE);
            RETURN;
      END;

      discard_single_record (i_table        => i_type,
                             i_id           => i_id,
                             i_target_scn   => l_scn);

      FOR l_row
         IN (    SELECT fk.table_name fk_table,
                        pt.table_name p_table,
                        FKC.COLUMN_NAME fk_column,
                        PtC.COLUMN_NAME p_column
                   FROM constraints_hlp fk
                        INNER JOIN CONS_COLUMNS_hlp fkc
                           ON FKC.CONSTRAINT_NAME = FK.CONSTRAINT_NAME
                        INNER JOIN constraints_hlp pk
                           ON     fk.table_name = pk.table_name
                              AND fk.CONSTRAINT_TYPE = 'R'
                              AND pk.CONSTRAINT_TYPE = 'P'
                        INNER JOIN constraints_hlp pt
                           ON FK.R_CONSTRAINT_NAME = PT.CONSTRAINT_NAME
                        INNER JOIN CONS_COLUMNS_hlp ptc
                           ON Ptc.CONSTRAINT_NAME = Pt.CONSTRAINT_NAME
                              AND FKC.POSITION = PtC.POSITION
                        INNER JOIN ADD_DISCARD_TABLES c
                           ON fk.table_name = c.table_name
                              AND C.ENTITY_TYPE = i_type
                  WHERE LEVEL = 1
             CONNECT BY fk.R_CONSTRAINT_NAME = PRIOR pk.CONSTRAINT_NAME
             START WITH pt.TABLE_NAME = i_type)
      LOOP
         discard_sub_records (i_type                => i_type,
                              i_table               => l_row.fk_table,
                              i_constraint_column   => l_row.fk_column,
                              i_parent_id           => i_id,
                              i_target_scn          => l_scn);
      END LOOP;

      set_is_reverting_entity (FALSE);
   END discard_entity;

   PROCEDURE discard_tx (i_tx_id IN NUMBER)
   AS
      l_tx_record      TRANSACTION%ROWTYPE;
      l_sqlStatement   VARCHAR2 (4000 CHAR) := '';
      l_dyn_cursor     SYS_REFCURSOR;
      l_entity_id      NUMBER;
   BEGIN
      SECURITY_PKG.INIT_CTX ('mdmDistribution');

      SELECT *
        INTO l_tx_record
        FROM TRANSACTION
       WHERE ID = i_tx_id;

      IF l_tx_record.status != 'REJECTED_FOR_DISCARD'
      THEN
         RAISE_APPLICATION_ERROR (
            -20300,
            'TRANSACTION has NOT THE state ''REJECTED_FOR_DISCARD''');
      END IF;

      set_is_reverting_tx (TRUE);
      l_sqlStatement :=
            'SELECT ID FROM '
         || DBMS_ASSERT.SQL_OBJECT_NAME (l_tx_record.entitytype)
         || ' WHERE transaction_id = '
         || i_tx_id;

      OPEN l_dyn_cursor FOR l_sqlStatement;

      FETCH l_dyn_cursor INTO l_entity_id;

      WHILE l_dyn_cursor%FOUND
      LOOP
         discard_entity (l_tx_record.entitytype, l_entity_id);

         FETCH l_dyn_cursor INTO l_entity_id;
      END LOOP;

      CLOSE l_dyn_cursor;

      DELETE FROM TRANSACTION
            WHERE ID = i_tx_id;

      set_is_reverting_tx (FALSE);
      SECURITY_PKG.REMOVE_CTX;
   END discard_tx;

   PROCEDURE cleanup_tx
   AS
      l_sqlStatement   VARCHAR2 (4000 CHAR);
   BEGIN
      SECURITY_PKG.INIT_CTX ('mdmDistribution');
      set_is_reverting_tx (TRUE);

      DELETE FROM transaction
            WHERE status = 'ALLOCATED';

      FOR l_type IN (SELECT DISTINCT entitytype FROM transaction)
      LOOP
         l_sqlStatement :=
            'DELETE FROM TRANSACTION WHERE ID IN '
            || '(SELECT t.ID FROM TRANSACTION t AS OF TIMESTAMP SYSTIMESTAMP - 1 LEFT OUTER JOIN '
            || l_type.entitytype
            || ' E ON t.id = e.transaction_id WHERE E.ID IS NULL AND t.) AND ENTITYTYPE=:type';
         DBMS_OUTPUT.put_line (
            l_sqlStatement || ' TYPE: ' || l_type.entitytype);

         EXECUTE IMMEDIATE l_sqlStatement USING l_type.entitytype;
      END LOOP;

      set_is_reverting_tx (FALSE);
      SECURITY_PKG.REMOVE_CTX;
   END cleanup_tx;
END transaction_pkg;
